package Inheritance;

public class OverloadingDemo {
	
	public void sum(int num1, int num2) {
		System.out.println("adding 2 int numbers");
	}
	public void sum(int num1, int num2,int num3) {
		System.out.println("adding 2 int numbers");
	}
	public void sum(float num1, float num2,float num3) {
		System.out.println("adding 2 int numbers");
	}
}
